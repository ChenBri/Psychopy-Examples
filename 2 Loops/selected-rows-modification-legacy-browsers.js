var t;
var frameN;
var continueRoutine;
var CreateComponents;
function CreateRoutineBegin(snapshot) {
  return async function () {
    TrialHandler.fromSnapshot(snapshot); // ensure that .thisN vals are up to date
    
    //--- Prepare to start Routine 'Create' ---
    t = 0;
    CreateClock.reset(); // clock
    frameN = -1;
    continueRoutine = true; // until we're told otherwise
    // update component parameters for each repeat
    // keep track of which components have finished
    CreateComponents = [];
    
    CreateComponents.forEach( function(thisComponent) {
      if ('status' in thisComponent)
        thisComponent.status = PsychoJS.Status.NOT_STARTED;
       });
    return Scheduler.Event.NEXT;
  }
}


function CreateRoutineEachFrame() {
  return async function () {
    //--- Loop for each frame of Routine 'Create' ---
    // get current time
    t = CreateClock.getTime();
    frameN = frameN + 1;// number of completed frames (so 0 is the first frame)
    // update/draw components on each frame
    // check for quit (typically the Esc key)
    if (psychoJS.experiment.experimentEnded || psychoJS.eventManager.getKeys({keyList:['escape']}).length > 0) {
      return quitPsychoJS('The [Escape] key was pressed. Goodbye!', false);
    }
    
    // check if the Routine should terminate
    if (!continueRoutine) {  // a component has requested a forced-end of Routine
      return Scheduler.Event.NEXT;
    }
    
    continueRoutine = false;  // reverts to True if at least one component still running
    CreateComponents.forEach( function(thisComponent) {
      if ('status' in thisComponent && thisComponent.status !== PsychoJS.Status.FINISHED) {
        continueRoutine = true;
      }
    });
    
    // refresh the screen if continuing
    if (continueRoutine) {
      return Scheduler.Event.FLIP_REPEAT;
    } else {
      return Scheduler.Event.NEXT;
    }
  };
}


function CreateRoutineEnd(snapshot) {
  return async function () {
    //--- Ending Routine 'Create' ---
    CreateComponents.forEach( function(thisComponent) {
      if (typeof thisComponent.setAutoDraw === 'function') {
        thisComponent.setAutoDraw(false);
      }
    });
    // the Routine "Create" was not non-slip safe, so reset the non-slip timer
    routineTimer.reset();
    
    // Routines running outside a loop should always advance the datafile row
    if (currentLoop === psychoJS.experiment) {
      psychoJS.experiment.nextEntry(snapshot);
    }
    return Scheduler.Event.NEXT;
  }
}


var loop_2;
function loop_2LoopBegin(loop_2LoopScheduler, snapshot) {
  return async function() {
    TrialHandler.fromSnapshot(snapshot); // update internal variables (.thisN etc) of the loop
    
    // set up handler to look after randomisation of conditions etc
    loop_2 = new TrialHandler({
      psychoJS: psychoJS,
      nReps: 1, method: TrialHandler.Method.RANDOM,
      extraInfo: expInfo, originPath: undefined,
      trialList: TrialHandler.importConditions(psychoJS.serverManager, 'loopTemplate2.xlsx', ((loop2Reps.toString() + ":") + (loop2Reps + 5).toString())),
      seed: undefined, name: 'loop_2'
    });
    psychoJS.experiment.addLoop(loop_2); // add the loop to the experiment
    currentLoop = loop_2;  // we're now the current loop
    
    // Schedule all the trials in the trialList:
    loop_2.forEach(function() {
      snapshot = loop_2.getSnapshot();
    
      loop_2LoopScheduler.add(importConditions(snapshot));
      const loop_1LoopScheduler = new Scheduler(psychoJS);
      loop_2LoopScheduler.add(loop_1LoopBegin(loop_1LoopScheduler, snapshot));
      loop_2LoopScheduler.add(loop_1LoopScheduler);
      loop_2LoopScheduler.add(loop_1LoopEnd);
      loop_2LoopScheduler.add(adRoutineBegin(snapshot));
      loop_2LoopScheduler.add(adRoutineEachFrame());
      loop_2LoopScheduler.add(adRoutineEnd(snapshot));
      loop_2LoopScheduler.add(trial_2RoutineBegin(snapshot));
      loop_2LoopScheduler.add(trial_2RoutineEachFrame());
      loop_2LoopScheduler.add(trial_2RoutineEnd(snapshot));
      loop_2LoopScheduler.add(loop_2LoopEndIteration(loop_2LoopScheduler, snapshot));
    });
    
    return Scheduler.Event.NEXT;
  }
}


